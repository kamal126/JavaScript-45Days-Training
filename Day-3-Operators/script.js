// Operators examples

// Arithmetic Operators
let a = 10;
let b = 5;

console.log("Arithmetic Operations:");
console.log(a + b); // Output: 15
console.log(a - b); // Output: 5
console.log(a * b); // Output: 50
console.log(a / b); // Output: 2
console.log(a % b); // Output: 0

// Assignment Operators
let x = 10;
x += 5; // x = x + 5
console.log("\nAfter Assignment Operation:");
console.log(x); // Output: 15

// Comparison Operators
console.log("\nComparison Operations:");
console.log(10 == '10'); // Output: true
console.log(10 === '10'); // Output: false

// Logical Operators
let isTrue = true;
let isFalse = false;

console.log("\nLogical Operations:");
console.log(isTrue && isFalse); // Output: false
console.log(isTrue || isFalse); // Output: true
console.log(!isTrue); // Output: false

// Conditional Operator
let age = 18;
let canVote = (age >= 18) ? "Yes" : "No";
console.log("\nCan Vote:");
console.log(canVote); // Output: Yes
